def get_scamper_categories():
    """SCAMPER 기법 카테고리 리스트를 반환합니다."""
    return list(SCAMPER_QUESTIONS.keys())

def get_questions_for_category(category_name):
    """주어진 카테고리에 해당하는 SCAMPER 질문 리스트를 반환합니다."""
    return SCAMPER_QUESTIONS.get(category_name, [])

def get_help_text_for_category(category_name):
    """주어진 카테고리에 해당하는 도움말 텍스트를 반환합니다."""
    return SCAMPER_HELP_TEXTS.get(category_name, "해당 카테고리에 대한 도움말이 준비되지 않았습니다.")

